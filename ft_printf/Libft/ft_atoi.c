#include "libft.h"

int	ft_atoi(const char *str)
{
	int					sign;
	unsigned long int	res;

	sign = 1;
	res = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		res = res * 10 + (*str - '0');
		str++;
		if (res > LONG_MAX && sign == 1)
			return (-1);
		if (res > LONG_MAX && sign == -1)
			return (0);
	}
	return ((int)(res * sign));
}
