#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*res;

	if (!s1 || !s2)
		return (NULL);
	res = (char *)malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!res)
		return (NULL);
	ft_memcpy(res, s1, ft_strlen(s1) + 1);
	ft_strlcat(res + ft_strlen(s1), s2, ft_strlen(s2) + 1);
	return (res);
}
